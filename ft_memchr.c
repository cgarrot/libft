#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned int	i;

	i = 0;
	while (i < n)
	{
		if ((const unsigned char)s == c)
			return ((const unsigned char)s);
		i++;
	}
	return (0);
}
